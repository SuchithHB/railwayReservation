export default function Navbar() {
  const { user, logout } = useAuth();
  const navigate = useNavigate();

  const handleLogout = () => {
    logout();
    navigate('/login');
  };

  return (
    <nav className="navbar">
      <NavLink to="/" className="navbar-brand">
        {Icons.train}
        RailReserve
      </NavLink>
      <div className="navbar-links">
        <NavLink to="/">{Icons.search} Search</NavLink>
        <NavLink to="/pnr">{Icons.pnr} PNR Status</NavLink>
        <NavLink to="/tracking">{Icons.track} Track</NavLink>
        {user && <NavLink to="/my-bookings">{Icons.ticket} My Bookings</NavLink>}
        {user?.role === 'ADMIN' && <NavLink to="/admin">{Icons.admin} Admin</NavLink>}
        {user ? (
          <button onClick={handleLogout}>{Icons.logout} Logout</button>
        ) : (
          <NavLink to="/login">{Icons.login} Login</NavLink>
        )}
      </div>
    </nav>
  );
}
